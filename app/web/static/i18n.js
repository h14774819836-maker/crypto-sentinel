let currentLang = localStorage.getItem('sentinel_lang') || 'zh';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('sentinel_lang', lang);
    applyTranslations();

    // Update all toggle button texts (desktop + mobile)
    const labels = lang === 'zh' ? 'EN' : 'ZH';
    document.querySelectorAll('[data-lang-toggle], #lang-toggle').forEach((toggleBtn) => {
        toggleBtn.textContent = labels;
    });
}

function toggleLanguage() {
    setLanguage(currentLang === 'zh' ? 'en' : 'zh');
}

function applyTranslations() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang] && translations[currentLang][key]) {
            el.textContent = translations[currentLang][key];
        }
    });

    // Also translate specific hardcoded status spans if they exist
    document.querySelectorAll('[data-i18n-enum]').forEach(el => {
        const key = el.getAttribute('data-i18n-enum');
        const type = el.getAttribute('data-i18n-type'); // e.g., 'action', 'bias'
        const fullKey = `${type}.${key.toLowerCase()}`;
        if (translations[currentLang] && translations[currentLang][fullKey]) {
            el.textContent = translations[currentLang][fullKey];
        }
    });
}

function tr(key, zhFallback, enFallback) {
    if (translations[currentLang] && translations[currentLang][key]) {
        return translations[currentLang][key];
    }
    return currentLang === 'zh' ? zhFallback : enFallback;
}

// Auto-run on DOM load
document.addEventListener('DOMContentLoaded', () => {
    setLanguage(currentLang);
});
